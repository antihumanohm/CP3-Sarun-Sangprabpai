num1 = int(input("กรอกตัวเลขตัวที่ 1 : "))
num2 = int(input("กรอกตัวเลขตัวที่ 2 : "))

print("ผลบวกของ",num1,"+",num2,"มีค่าเท่ากับ :",num1+num2)
print("ผลลบของ",num1,"-",num2,"มีค่าเท่ากับ :",num1-num2)
print("ผลคูณของ",num1,"*",num2,"มีค่าเท่ากับ :",num1*num2)
print("ผลหารของ",num1,"/",num2,"มีค่าเท่ากับ :",num1/num2)